/**
 * Convex orchestration surfaces: `convex.crons`, `convex.http`, `convex.sagas`.
 *
 * - Schedules → `convex/crons.ts` (`cronJobs()` scheduling the command mutations).
 * - Webhooks  → `convex/http.ts` (`httpRouter` + `httpAction` invoking commands).
 * - Sagas     → `convex/sagas.ts` (orchestrator `action`s that run each step via
 *   `ctx.runMutation`, compensating completed steps in reverse on failure).
 *
 * Convex has no native saga/webhook/cron-of-commands primitive, so these are
 * faithful structural projections onto Convex's scheduler / HTTP / action APIs.
 * Step argument mapping is not expressed in the saga IR, so the orchestrator
 * forwards a single `input` payload to each step (app-supplied).
 */

import type { IR, IRSchedule, IRWebhook, IRSaga } from '../../ir';
import type { ProjectionDiagnostic } from '../interface';
import { normalizeOptions } from './options.js';
import { renderExpression, DEFAULT_GLOBALS } from './expression.js';

export interface OrchestrationResult {
  code: string;
  diagnostics: ProjectionDiagnostic[];
}

const HEADER = '// GENERATED by the Manifest → Convex projection. DO NOT EDIT.';

/** Mutation export name for a command on an entity (matches convex.mutations). */
function mutationRef(entity: string, command: string): string {
  return `api.mutations.${entity}_${command}`;
}

// ---------------------------------------------------------------------------
// Schedules → crons
// ---------------------------------------------------------------------------

function intervalArg(durationMs: number): string {
  if (durationMs % 3_600_000 === 0) return `{ hours: ${durationMs / 3_600_000} }`;
  if (durationMs % 60_000 === 0) return `{ minutes: ${durationMs / 60_000} }`;
  return `{ seconds: ${Math.max(1, Math.round(durationMs / 1000))} }`;
}

export function generateCrons(
  ir: IR,
  rawOptions: Record<string, unknown> | undefined,
): OrchestrationResult {
  // Validate/normalize options even though crons currently ignore the bag.
  normalizeOptions(rawOptions);
  const diagnostics: ProjectionDiagnostic[] = [];
  const lines: string[] = [];

  for (const sched of (ir.schedules ?? []) as IRSchedule[]) {
    const ref = sched.entityName
      ? mutationRef(sched.entityName, sched.commandName)
      : `api.mutations.${sched.commandName}`;
    const paramsObj = `{${(sched.params ?? [])
      .map((p) => {
        const { code, unresolved } = renderExpression(p.expression, { selfVar: 'args' });
        if (unresolved.length)
          diagnostics.push({
            severity: 'warning',
            code: 'CONVEX_UNRESOLVED_SCHEDULE_PARAM',
            message: `schedule '${sched.name}' param '${p.name}' unresolved; omitted.`,
          });
        return unresolved.length ? null : ` ${p.name}: ${code}`;
      })
      .filter(Boolean)
      .join(',')} }`;
    const t = sched.trigger;
    if (t.kind === 'cron' && t.cron) {
      lines.push(
        `crons.cron(${JSON.stringify(sched.name)}, ${JSON.stringify(t.cron)}, ${ref}, ${paramsObj});`,
      );
    } else if (t.durationMs) {
      lines.push(
        `crons.interval(${JSON.stringify(sched.name)}, ${intervalArg(t.durationMs)}, ${ref}, ${paramsObj});`,
      );
    } else {
      diagnostics.push({
        severity: 'warning',
        code: 'CONVEX_UNRESOLVED_TRIGGER',
        message: `schedule '${sched.name}' has no cron/duration; skipped.`,
      });
    }
  }

  if (lines.length === 0)
    diagnostics.push({
      severity: 'info',
      code: 'CONVEX_NO_SCHEDULES',
      message: 'No schedules declared; emitted an empty crons file.',
    });

  const code =
    `${HEADER}\n// ${lines.length} cron job(s).\n\n` +
    `import { cronJobs } from "convex/server";\n` +
    `import { api } from "./_generated/api";\n\n` +
    `const crons = cronJobs();\n\n` +
    `${lines.join('\n')}${lines.length ? '\n\n' : '\n'}` +
    `export default crons;\n`;
  return { code, diagnostics };
}

// ---------------------------------------------------------------------------
// Webhooks → http
// ---------------------------------------------------------------------------

/**
 * Derive a SCREAMING_SNAKE_CASE environment-variable name from an IR context
 * path string (e.g. `"context.stripeWebhookSecret"` → `"STRIPE_WEBHOOK_SECRET"`).
 * Strips a leading `context.` / `user.` segment, then converts camelCase
 * segments to UPPER_SNAKE.
 */
function secretToEnvVar(contextPath: string): string {
  const parts = contextPath.split('.');
  const segments = parts[0] === 'context' || parts[0] === 'user' ? parts.slice(1) : parts;
  return segments
    .map((s) => s.replace(/([a-z])([A-Z])/g, '$1_$2'))
    .join('_')
    .toUpperCase();
}

/**
 * Inline helper functions emitted when any webhook declares HMAC signature
 * verification. Uses Web Crypto `subtle.verify` for constant-time comparison.
 */
const HMAC_HELPERS =
  `/** Strip a sha256= / sha512= prefix from a signature value (GitHub convention). */\n` +
  `function _stripSigPrefix(sig: string): string {\n` +
  `  const eq = sig.indexOf("=");\n` +
  `  if (eq > 0) {\n` +
  `    const pre = sig.slice(0, eq).toLowerCase();\n` +
  `    if (pre === "sha256" || pre === "sha512") return sig.slice(eq + 1);\n` +
  `  }\n` +
  `  return sig;\n` +
  `}\n\n` +
  `/** Decode a hex string to an ArrayBuffer; returns null on invalid input. */\n` +
  `function _hexToBuffer(hex: string): ArrayBuffer | null {\n` +
  `  if (hex.length % 2 !== 0) return null;\n` +
  `  const arr = new Uint8Array(hex.length / 2);\n` +
  `  for (let i = 0; i < hex.length; i += 2) {\n` +
  `    const b = parseInt(hex.slice(i, i + 2), 16);\n` +
  `    if (Number.isNaN(b)) return null;\n` +
  `    arr[i >> 1] = b;\n` +
  `  }\n` +
  `  return arr.buffer;\n` +
  `}\n\n` +
  `/**\n` +
  ` * Constant-time HMAC verification via Web Crypto subtle.verify.\n` +
  ` * Returns false for any malformed or non-matching signature.\n` +
  ` */\n` +
  `async function _verifyHmac(\n` +
  `  rawBody: string,\n` +
  `  algorithm: "hmac-sha256" | "hmac-sha512",\n` +
  `  secret: string,\n` +
  `  provided: string,\n` +
  `): Promise<boolean> {\n` +
  `  const hashAlg = algorithm === "hmac-sha512" ? "SHA-512" : "SHA-256";\n` +
  `  const sigBytes = _hexToBuffer(_stripSigPrefix(provided).toLowerCase());\n` +
  `  if (!sigBytes) return false;\n` +
  `  const cryptoKey = await crypto.subtle.importKey(\n` +
  `    "raw",\n` +
  `    new TextEncoder().encode(secret),\n` +
  `    { name: "HMAC", hash: hashAlg },\n` +
  `    false,\n` +
  `    ["verify"],\n` +
  `  );\n` +
  `  return crypto.subtle.verify("HMAC", cryptoKey, sigBytes, new TextEncoder().encode(rawBody));\n` +
  `}`;

export function generateHttp(
  ir: IR,
  rawOptions: Record<string, unknown> | undefined,
): OrchestrationResult {
  const options = normalizeOptions(rawOptions);
  const diagnostics: ProjectionDiagnostic[] = [];

  const webhooks = (ir.webhooks ?? []) as IRWebhook[];
  const hasSignature = webhooks.some((w) => !!w.signature);
  const hasIdempotency = webhooks.some((w) => !!w.idempotencyHeader);

  // --- Per-route code blocks ---
  const routes: string[] = [];

  for (const wh of webhooks) {
    const ref = wh.entity ? mutationRef(wh.entity, wh.command) : `api.mutations.${wh.command}`;
    const method = (wh.method ?? 'POST').toUpperCase();
    const bodyLines = buildWebhookHandlerBody(wh, ref, diagnostics);
    routes.push(
      `http.route({\n` +
        `  path: ${JSON.stringify(wh.path)},\n` +
        `  method: ${JSON.stringify(method)},\n` +
        `  handler: httpAction(async (ctx, request) => {\n` +
        `${bodyLines.join('\n')}\n` +
        `  }),\n` +
        `});`,
    );
  }

  if (routes.length === 0) {
    diagnostics.push({
      severity: 'info',
      code: 'CONVEX_NO_WEBHOOKS',
      message: 'No webhooks declared; emitted an empty http router.',
    });
  }

  // --- Idempotency mutation (exported so Convex registers it as internal.http._checkIdempotencyKey) ---
  const idempotencyMutation = hasIdempotency
    ? `/** Atomic idempotency check-and-set. Returns true if the key is new, false if already seen. */\n` +
      `export const _checkIdempotencyKey = internalMutation({\n` +
      `  args: { key: v.string(), webhookName: v.string() },\n` +
      `  handler: async (ctx, { key, webhookName }) => {\n` +
      `    const existing = await ctx.db\n` +
      `      .query(${JSON.stringify(options.idempotencyTable)})\n` +
      `      .withIndex("by_key", (q) => q.eq("key", key))\n` +
      `      .first();\n` +
      `    if (existing !== null) return false;\n` +
      `    await ctx.db.insert(${JSON.stringify(options.idempotencyTable)}, { key, webhookName, seenAt: Date.now() });\n` +
      `    return true;\n` +
      `  },\n` +
      `});`
    : '';

  // --- Imports ---
  const serverImports = hasIdempotency ? `httpAction, internalMutation` : `httpAction`;
  const apiImports = hasIdempotency ? `api, internal` : `api`;
  const importLines = [
    `import { httpRouter } from "convex/server";`,
    `import { ${serverImports} } from "./_generated/server";`,
    `import { ${apiImports} } from "./_generated/api";`,
    ...(hasIdempotency ? [`import { v } from "convex/values";`] : []),
  ];

  const code =
    `${HEADER}\n// ${routes.length} webhook route(s).\n\n` +
    `${importLines.join('\n')}\n` +
    (hasSignature ? `\n${HMAC_HELPERS}\n` : '') +
    (hasIdempotency ? `\n${idempotencyMutation}\n` : '') +
    `\nconst http = httpRouter();\n\n` +
    `${routes.join('\n\n')}${routes.length ? '\n\n' : '\n'}` +
    `export default http;\n`;
  return { code, diagnostics };
}

function buildWebhookHandlerBody(
  wh: IRWebhook,
  mutationRefPath: string,
  diagnostics: ProjectionDiagnostic[],
): string[] {
  const needsRawBody = !!wh.signature;
  const bodyLines: string[] = [];
  if (needsRawBody) {
    bodyLines.push(`    const _rawBody = await request.text();`);
  }
  bodyLines.push(...webhookSignatureLines(wh));
  bodyLines.push(...webhookIdempotencyLines(wh));
  bodyLines.push(...webhookBodyParseLines(needsRawBody));
  bodyLines.push(...webhookDispatchLines(wh, mutationRefPath, diagnostics));
  return bodyLines;
}

function webhookSignatureLines(wh: IRWebhook): string[] {
  if (!wh.signature) return [];
  const sig = wh.signature;
  const envVar = secretToEnvVar(sig.secret);
  return [
    `    const _secret = process.env[${JSON.stringify(envVar)}];`,
    `    if (!_secret)`,
    `      return new Response(JSON.stringify({ error: "Webhook '${wh.name}' signature secret not configured" }), { status: 500, headers: { "Content-Type": "application/json" } });`,
    `    const _sig = request.headers.get(${JSON.stringify(sig.header)});`,
    `    if (!_sig)`,
    `      return new Response(JSON.stringify({ error: "Missing signature header '${sig.header}'" }), { status: 401, headers: { "Content-Type": "application/json" } });`,
    `    if (!(await _verifyHmac(_rawBody, ${JSON.stringify(sig.algorithm)}, _secret, _sig)))`,
    `      return new Response(JSON.stringify({ error: "Invalid webhook signature" }), { status: 401, headers: { "Content-Type": "application/json" } });`,
  ];
}

function webhookIdempotencyLines(wh: IRWebhook): string[] {
  if (!wh.idempotencyHeader) return [];
  return [
    `    const _ikey = request.headers.get(${JSON.stringify(wh.idempotencyHeader)});`,
    `    if (!_ikey)`,
    `      return new Response(JSON.stringify({ error: "Missing idempotency header '${wh.idempotencyHeader}'" }), { status: 400, headers: { "Content-Type": "application/json" } });`,
    `    const _isNew = await ctx.runMutation(internal.http._checkIdempotencyKey, { key: _ikey, webhookName: ${JSON.stringify(wh.name)} });`,
    `    if (!_isNew) return new Response(null, { status: 200 }); // duplicate delivery`,
  ];
}

function webhookBodyParseLines(needsRawBody: boolean): string[] {
  if (needsRawBody) {
    return [
      `    let body: unknown;`,
      `    try { body = JSON.parse(_rawBody); } catch { return new Response(JSON.stringify({ error: "Invalid JSON body" }), { status: 400 }); }`,
    ];
  }
  return [`    const body = await request.json();`];
}

function webhookDispatchLines(
  wh: IRWebhook,
  mutationRefPath: string,
  diagnostics: ProjectionDiagnostic[],
): string[] {
  const transformEntries: string[] = [];
  for (const t of wh.transform ?? []) {
    const { code, unresolved } = renderExpression(t.expression, {
      selfVar: 'body',
      globals: ['body', ...DEFAULT_GLOBALS],
    });
    if (unresolved.length) {
      diagnostics.push({
        severity: 'warning',
        code: 'CONVEX_UNRESOLVED_WEBHOOK_PARAM',
        message: `webhook '${wh.name}' param '${t.name}' unresolved; omitted.`,
      });
      continue;
    }
    transformEntries.push(`${t.name}: ${code}`);
  }
  return [
    `    await ctx.runMutation(${mutationRefPath}, { ${transformEntries.join(', ')} } as any);`,
    `    return new Response(null, { status: 200 });`,
  ];
}

// ---------------------------------------------------------------------------
// Sagas → orchestrator actions
// ---------------------------------------------------------------------------

export function generateSagas(
  ir: IR,
  _rawOptions: Record<string, unknown> | undefined,
): OrchestrationResult {
  const diagnostics: ProjectionDiagnostic[] = [];
  const blocks: string[] = [];

  for (const saga of (ir.sagas ?? []) as IRSaga[]) {
    const forward: string[] = [];
    saga.steps.forEach((step, i) => {
      forward.push(
        `      await ctx.runMutation(${mutationRef(step.commandEntity, step.command)}, input as any);`,
      );
      forward.push(`      completed.push(${i});`);
    });

    // Compensation in reverse for completed steps that declare a compensate command.
    const comp: string[] = [];
    if (saga.onFailure === 'compensate') {
      for (let i = saga.steps.length - 1; i >= 0; i--) {
        const step = saga.steps[i];
        if (!step.compensate || !step.compensateEntity) continue;
        comp.push(
          `      if (completed.includes(${i})) await ctx.runMutation(${mutationRef(step.compensateEntity, step.compensate)}, input as any);`,
        );
      }
    }

    blocks.push(
      `export const ${saga.name} = action({\n` +
        `  args: { input: v.any() },\n` +
        `  handler: async (ctx, { input }) => {\n` +
        `    const completed: number[] = [];\n` +
        `    try {\n` +
        `${forward.join('\n')}\n` +
        `      return { ok: true, completed };\n` +
        `    } catch (err) {\n` +
        (comp.length
          ? `      // compensate completed steps in reverse\n${comp.join('\n')}\n`
          : `      // onFailure: ${saga.onFailure} — no compensation\n`) +
        `      throw err;\n` +
        `    }\n` +
        `  },\n});`,
    );
  }

  if (blocks.length === 0)
    diagnostics.push({
      severity: 'info',
      code: 'CONVEX_NO_SAGAS',
      message: 'No sagas declared; emitted an empty sagas file.',
    });

  const code =
    `${HEADER}\n// ${blocks.length} saga orchestrator(s).\n\n` +
    `import { action } from "./_generated/server";\n` +
    `import { v } from "convex/values";\n` +
    `import { api } from "./_generated/api";\n\n` +
    `${blocks.join('\n\n')}${blocks.length ? '\n' : ''}`;
  return { code, diagnostics };
}
