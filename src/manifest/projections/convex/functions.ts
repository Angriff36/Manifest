function capWord(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

interface QueryIndexField {
  /** Column being matched (also the arg name). */
  name: string;
  /** Target table when the column is a convexId reference (→ `v.id(...)` arg). */
  fkTarget?: string;
}

interface QueryIndexSpec {
  /** Columns the index covers, in order (single-field or composite). */
  fields: QueryIndexField[];
  /** Schema index name to read via `withIndex(...)`. */
  indexName: string;
}

/**
 * The indexes an entity exposes as reads, derived to match EXACTLY what the
 * schema surface emits (`generator.ts#emitTable`): `indexed`-modifier properties,
 * the tenant column, every belongsTo/ref FK (in BOTH reference modes), and every
 * `options.indexes` entry (single-field AND composite). Single-field specs are
 * deduped by column in schema order; composite specs by index name. The result
 * guarantees every schema index has a corresponding `list<Entity>By<Field...>`
 * query — the two surfaces can no longer drift (closing both the stringId-mode
 * FK gap and the composite-index gap).
 */
function collectQueryIndexSpecs(ir: IR, entity: IREntity, options: Normalized): QueryIndexSpec[] {
  const specs: QueryIndexSpec[] = [];
  const seenSingle = new Set<string>();
  const seenName = new Set<string>();
  // convexId-only targets: present → arg typed `v.id(target)`; absent → `v.string()`.
  const fkTargets = collectFkTargets(entity, ir, options);
  const mkField = (name: string): QueryIndexField => ({ name, fkTarget: fkTargets.get(name) });

  const addSingle = (field: string, indexName: string): void => {
    if (field === 'id' || seenSingle.has(field)) return;
    seenSingle.add(field);
    seenName.add(indexName);
    specs.push({ fields: [mkField(field)], indexName });
  };

  for (const p of entity.properties) {
    if (p.modifiers.includes('indexed')) addSingle(p.name, `by_${p.name}`);
  }
  const tenantProp = ir.tenant?.property;
  if (tenantProp && entity.properties.some((p) => p.name === tenantProp))
    addSingle(tenantProp, `by_${tenantProp}`);
  for (const fk of collectReferenceFields(entity, ir, options).keys()) addSingle(fk, `by_${fk}`);
  for (const entry of options.indexes[entity.name] ?? []) {
    const def = indexEntryToDef(entry);
    if (def.fields.length === 1) {
      addSingle(def.fields[0], def.name);
    } else if (def.fields.length > 1 && !seenName.has(def.name)) {
      seenName.add(def.name);
      specs.push({ fields: def.fields.map(mkField), indexName: def.name });
    }
  }
  return specs;
}

/**
 * Tenant-id binding for a handler body. Identity comes from the author-owned
 * `getAuthContext(ctx)` module ({@link NormalizedOptions.authContextImport}).
 * Generation fails with `CONVEX_AUTH_CONTEXT_REQUIRED` when tenant filtering is
 * on without that import — this path is never reached for the broken legacy
 * `(ctx as any).auth` bag.
 */
function tenantBindingLine(_options: Normalized, tenantProp: string): string {
  return `    const __tenant = ((await getAuthContext(ctx)) as any).${tenantProp} ?? null;`;
}

/** Resolved read-side tenant + soft-delete filtering for an entity (field-aware). */
interface ReadFilter {
  hasTenant: boolean;
  tenantProp: string | undefined;
  /** The `by_<tenantProp>` index exists in the schema (IR-declared tenant). */
  tenantIndexed: boolean;
  hasSoftDelete: boolean;
  deletedProp: string;
}

function resolveReadFilter(ir: IR, entity: IREntity, options: Normalized): ReadFilter {
  const tenantProp = options.tenantIdProperty ?? ir.tenant?.property;
  // Tenant scoping requires a real identity seam — never the ineffective ctx.auth bag.
  const hasTenant =
    options.includeTenantFilter &&
    !!options.authContextImport &&
    !!tenantProp &&
    entity.properties.some((p) => p.name === tenantProp);
  const deletedProp = options.deletedAtProperty;
  const hasSoftDelete =
    options.includeSoftDeleteFilter && entity.properties.some((p) => p.name === deletedProp);
  return {
    hasTenant,
    tenantProp: hasTenant ? tenantProp : undefined,
    tenantIndexed: hasTenant && tenantProp === ir.tenant?.property,
    hasSoftDelete,
    deletedProp,
  };
}

type PolicyPrelude = (deniedValue: '[]' | 'null') => string[];
type FinishRows = (rowsExpr: string) => string;
type FinishDoc = (docExpr: string) => string;

function emitListEntityQuery(
  blocks: string[],
  entity: IREntity,
  table: string,
  rf: ReadFilter,
  options: Normalized,
  qfn: string,
  policyPrelude: PolicyPrelude,
  finishRows: FinishRows,
): void {
  if (!rf.hasTenant && !rf.hasSoftDelete) {
    const bodyLines = [
      ...policyPrelude('[]'),
      `    ${finishRows(`await ctx.db.query("${table}").collect()`)}`,
    ].join('\n');
    blocks.push(
      `export const list${entity.name} = ${qfn}({\n` +
        `  args: {},\n` +
        `  handler: async (ctx) => {\n` +
        `${bodyLines}\n` +
        `  },\n});`,
    );
    return;
  }
  const lines: string[] = [...policyPrelude('[]')];
  if (rf.hasTenant) lines.push(tenantBindingLine(options, rf.tenantProp!));
  if (rf.hasTenant && rf.tenantIndexed) {
    lines.push(
      `    let rows = await ctx.db.query("${table}").withIndex("by_${rf.tenantProp}", (q) => q.eq("${rf.tenantProp}", __tenant)).collect();`,
    );
  } else {
    lines.push(`    let rows = await ctx.db.query("${table}").collect();`);
    if (rf.hasTenant) {
      lines.push(`    rows = rows.filter((d) => (d as any).${rf.tenantProp} === __tenant);`);
    }
  }
  if (rf.hasSoftDelete) {
    lines.push(`    rows = rows.filter((d) => (d as any).${rf.deletedProp} == null);`);
  }
  lines.push(`    ${finishRows('rows')}`);
  blocks.push(
    `export const list${entity.name} = ${qfn}({\n` +
      `  args: {},\n` +
      `  handler: async (ctx) => {\n${lines.join('\n')}\n  },\n});`,
  );
}

function emitGetEntityQuery(
  blocks: string[],
  entity: IREntity,
  table: string,
  rf: ReadFilter,
  options: Normalized,
  qfn: string,
  policyPrelude: PolicyPrelude,
  finishDoc: FinishDoc,
): void {
  if (!rf.hasTenant && !rf.hasSoftDelete) {
    const bodyLines = [...policyPrelude('null'), `    ${finishDoc(`await ctx.db.get(id)`)}`].join(
      '\n',
    );
    blocks.push(
      `export const get${entity.name} = ${qfn}({\n` +
        `  args: { id: v.id("${table}") },\n` +
        `  handler: async (ctx, { id }) => {\n` +
        `${bodyLines}\n` +
        `  },\n});`,
    );
    return;
  }
  const lines: string[] = [...policyPrelude('null'), `    const doc = await ctx.db.get(id);`];
  if (rf.hasTenant) {
    lines.push(tenantBindingLine(options, rf.tenantProp!));
    lines.push(`    if (doc && (doc as any).${rf.tenantProp} !== __tenant) return null;`);
  }
  if (rf.hasSoftDelete) {
    lines.push(`    if (doc && (doc as any).${rf.deletedProp} != null) return null;`);
  }
  lines.push(`    ${finishDoc('doc')}`);
  blocks.push(
    `export const get${entity.name} = ${qfn}({\n` +
      `  args: { id: v.id("${table}") },\n` +
      `  handler: async (ctx, { id }) => {\n${lines.join('\n')}\n  },\n});`,
  );
}

function emitListByIndexQueries(
  blocks: string[],
  ir: IR,
  entity: IREntity,
  table: string,
  rf: ReadFilter,
  options: Normalized,
  qfn: string,
  policyPrelude: PolicyPrelude,
  finishRows: FinishRows,
): void {
  for (const spec of collectQueryIndexSpecs(ir, entity, options)) {
    const names = spec.fields.map((f) => f.name);
    const suffix = spec.fields.map((f) => capWord(f.name)).join('And');
    const argList = spec.fields
      .map((f) => `${f.name}: ${f.fkTarget ? `v.id("${f.fkTarget}")` : 'v.string()'}`)
      .join(', ');
    const destructure = `{ ${names.join(', ')} }`;
    const eqChain = spec.fields.map((f) => `.eq("${f.name}", ${f.name})`).join('');
    const applyTenant = rf.hasTenant && !!rf.tenantProp && !names.includes(rf.tenantProp);

    if (!applyTenant && !rf.hasSoftDelete) {
      const bodyLines = [
        ...policyPrelude('[]'),
        `    ${finishRows(
          `await ctx.db.query("${table}").withIndex("${spec.indexName}", (q) => q${eqChain}).collect()`,
        )}`,
      ].join('\n');
      blocks.push(
        `export const list${entity.name}By${suffix} = ${qfn}({\n` +
          `  args: { ${argList} },\n` +
          `  handler: async (ctx, ${destructure}) => {\n` +
          `${bodyLines}\n` +
          `  },\n});`,
      );
      continue;
    }

    const lines: string[] = [...policyPrelude('[]')];
    if (applyTenant) lines.push(tenantBindingLine(options, rf.tenantProp!));
    lines.push(
      `    let rows = await ctx.db.query("${table}").withIndex("${spec.indexName}", (q) => q${eqChain}).collect();`,
    );
    if (applyTenant) {
      lines.push(`    rows = rows.filter((d) => (d as any).${rf.tenantProp} === __tenant);`);
    }
    if (rf.hasSoftDelete) {
      lines.push(`    rows = rows.filter((d) => (d as any).${rf.deletedProp} == null);`);
    }
    lines.push(`    ${finishRows('rows')}`);
    blocks.push(
      `export const list${entity.name}By${suffix} = ${qfn}({\n` +
        `  args: { ${argList} },\n` +
        `  handler: async (ctx, ${destructure}) => {\n${lines.join('\n')}\n  },\n});`,
    );
  }
}

export function generateQueries(
  ir: IR,
  rawOptions: Record<string, unknown> | undefined,
): FunctionsResult {
  const options = normalizeOptions(rawOptions);
  const diagnostics: ProjectionDiagnostic[] = [];
  const blocks: string[] = [];

  for (const entity of persistentEntities(ir)) {
    const visibility = resolveConvexReadVisibility(
      ir,
      entity.name,
      options.authContextImport,
      '__row',
    );
    const listPolicyPlan = visibility.policies;
    const docPolicyPlan = renderReadPolicies(ir, entity.name, '__doc');
    diagnostics.push(...listPolicyPlan.diagnostics);
    const policyGated = visibility.gated;
    const policyEnforceable = visibility.clientReadable;
    const qfn = policyEnforceable ? 'query' : 'internalQuery';
    const table = resolveConvexTableName(entity.name, options);
    const rf = resolveReadFilter(ir, entity, options);
    const privates = privateFieldNames(entity);
    const encrypted = encryptedFieldNames(entity);
    const inlineComputed =
      options.computedProperties === 'inline'
        ? renderInlineComputedFields(entity, '__row', options)
        : {
            fields: [] as { name: string; code: string }[],
            diagnostics: [] as ProjectionDiagnostic[],
          };
    diagnostics.push(...inlineComputed.diagnostics);
    const allPolicyChecks = [...listPolicyPlan.contextChecks, ...listPolicyPlan.rowChecks];
    const policyAuthLines = (): string[] => {
      const text = allPolicyChecks.map((check) => check.code).join('\n');
      const needsUser = /\buser\b/.test(text);
      const needsContext = /\bcontext\b/.test(text);
      if (!needsUser && !needsContext) return [];
      return [
        `    const __auth = (await getAuthContext(ctx)) as any;`,
        ...(needsUser ? [`    const user = (__auth.user ?? __auth) as any;`] : []),
        ...(needsContext ? [`    const context = (__auth.context ?? __auth) as any;`] : []),
      ];
    };
    const policyPrelude = (deniedValue: '[]' | 'null'): string[] => {
      if (!policyGated || !policyEnforceable) return [];
      return [
        ...policyAuthLines(),
        ...listPolicyPlan.contextChecks.map(
          (check) =>
            `    if (!__allowsRead(${JSON.stringify(check.policyName)}, ${JSON.stringify(entity.name)}, () => ${check.code})) return ${deniedValue};`,
        ),
      ];
    };

    /** Finalize rows: decrypt → read policies → computed/private public projection. */
    const finishRows = (rowsExpr: string): string => {
      if (encrypted.length === 0 && !policyGated) {
        if (inlineComputed.fields.length === 0) {
          return stripPrivateFromRows(rowsExpr, privates);
        }
        const assigns = inlineComputed.fields.map((f) => `${f.name}: ${f.code}`).join(', ');
        const mapped = `(${rowsExpr}).map((__row) => ({ ...(__row as any), ${assigns} }))`;
        return stripPrivateFromRows(mapped, privates);
      }

      const lines: string[] = [];
      const plainExpr =
        encrypted.length > 0 && options.encryptionImport
          ? `await Promise.all((${rowsExpr}).map((row) => __decryptDoc(ctx, ${JSON.stringify(entity.name)}, ${JSON.stringify(encrypted)}, row)))`
          : rowsExpr;
      lines.push(`const __plainRows = ${plainExpr};`);
      let visibleExpr = '__plainRows';
      if (policyGated && policyEnforceable) {
        if (listPolicyPlan.rowChecks.length > 0) {
          lines.push(`    const __visibleRows: any[] = [];`);
          lines.push(`    for (const __row of __plainRows) {`);
          for (const check of listPolicyPlan.rowChecks) {
            lines.push(
              `      if (!__allowsRead(${JSON.stringify(check.policyName)}, ${JSON.stringify(entity.name)}, () => ${check.code})) continue;`,
            );
          }
          lines.push(`      __visibleRows.push(__row);`);
          lines.push(`    }`);
          visibleExpr = '__visibleRows';
        }
      }
      if (inlineComputed.fields.length > 0) {
        const assigns = inlineComputed.fields.map((f) => `${f.name}: ${f.code}`).join(', ');
        visibleExpr = `(${visibleExpr}).map((__row) => ({ ...(__row as any), ${assigns} }))`;
      }
      lines.push(`    ${stripPrivateFromRows(visibleExpr, privates)}`);
      return lines.join('\n');
    };
    /** Finalize one doc: decrypt → read policies → computed/private projection. */
    const finishDoc = (docExpr: string): string => {
      if (encrypted.length === 0 && !policyGated) {
        if (inlineComputed.fields.length === 0) {
          return stripPrivateFromDoc(docExpr, privates);
        }
        const assigns = inlineComputed.fields
          .map((f) => `${f.name}: ${f.code.replace(/\b__row\b/g, '__doc')}`)
          .join(', ');
        if (privates.length === 0) {
          return (
            `const __doc = ${docExpr};\n` +
            `    if (!__doc) return __doc;\n` +
            `    return { ...(__doc as any), ${assigns} };`
          );
        }
        const dels = privates.map((p) => `delete (__out as any).${p};`).join(' ');
        return (
          `const __doc = ${docExpr};\n` +
          `    if (!__doc) return __doc;\n` +
          `    const __out = { ...(__doc as any), ${assigns} };\n` +
          `    ${dels}\n` +
          `    return __out;`
        );
      }

      const lines: string[] = [
        `const __rawDoc = ${docExpr};`,
        `    if (!__rawDoc) return __rawDoc;`,
      ];
      if (encrypted.length > 0 && options.encryptionImport) {
        lines.push(
          `    const __doc = await __decryptDoc(ctx, ${JSON.stringify(entity.name)}, ${JSON.stringify(encrypted)}, __rawDoc);`,
        );
      } else {
        lines.push(`    const __doc = __rawDoc;`);
      }
      if (policyGated && policyEnforceable) {
        for (const check of docPolicyPlan.rowChecks) {
          lines.push(
            `    if (!__allowsRead(${JSON.stringify(check.policyName)}, ${JSON.stringify(entity.name)}, () => ${check.code})) return null;`,
          );
        }
      }
      const assigns = inlineComputed.fields
        .map((f) => `${f.name}: ${f.code.replace(/\b__row\b/g, '__doc')}`)
        .join(', ');
      if (privates.length === 0) {
        lines.push(
          inlineComputed.fields.length > 0
            ? `    return { ...(__doc as any), ${assigns} };`
            : `    return __doc;`,
        );
        return lines.join('\n');
      }
      const dels = privates.map((p) => `delete (__out as any).${p};`).join(' ');
      lines.push(
        `    const __out = { ...(__doc as any)${assigns ? `, ${assigns}` : ''} };`,
        `    ${dels}`,
        `    return __out;`,
      );
      return lines.join('\n');
    };

    // list/get/listBy — tenant + soft-delete filters applied in helpers.
    emitListEntityQuery(blocks, entity, table, rf, options, qfn, policyPrelude, finishRows);
    emitGetEntityQuery(blocks, entity, table, rf, options, qfn, policyPrelude, finishDoc);
    emitListByIndexQueries(
      blocks,
      ir,
      entity,
      table,
      rf,
      options,
      qfn,
      policyPrelude,
      finishRows,
    );
  }

  // System events table reads (when emitted): recent feed + the three indexed
  // lookups (by_type / by_entity / by_entityId) the schema declares on it.
  if (options.emitEventsTable) {
    // Event rows carry payloads from every entity, so ANY read/all policy in
    // the program locks the events feed down too — unless an auth seam is set
    // (same rule as entity queries: public + tenant via getAuthContext).
    const anyReadPolicy = ir.policies.some((p) => p.action === 'read' || p.action === 'all');
    const evq = anyReadPolicy ? 'internalQuery' : 'query';
    const ev = resolveEventsTableName(ir, options);
    blocks.push(
      `export const listRecentEvents = ${evq}({\n` +
        `  args: {},\n` +
        `  handler: async (ctx) => {\n` +
        `    return await ctx.db.query("${ev}").order("desc").take(50);\n` +
        `  },\n});`,
    );
    for (const field of ['type', 'entity', 'entityId']) {
      blocks.push(
        `export const listEventsBy${capWord(field)} = ${evq}({\n` +
          `  args: { ${field}: v.string() },\n` +
          `  handler: async (ctx, { ${field} }) => {\n` +
          `    return await ctx.db.query("${ev}").withIndex("by_${field}", (q) => q.eq("${field}", ${field})).collect();\n` +
          `  },\n});`,
      );
    }
  }

  const body = blocks.join('\n\n');
  const needsAuthCtx = !!options.authContextImport && /\bgetAuthContext\b/.test(body);
  const needsDecrypt = !!options.encryptionImport && /\b__decryptDoc\b/.test(body);
  const helpers: string[] = [];
  if (/\b__allowsRead\(/.test(body)) helpers.push(READ_POLICY_HELPER);
  if (/\bcheckRole\(/.test(body)) {
    helpers.push(
      `const ROLE_PERMISSIONS: Record<string, { action: string; target?: string }[]> = ${roleMapLiteral(ir)};\n\n` +
        `function checkRole(userRole: unknown, action: unknown, target?: unknown): boolean {\n` +
        `  if (typeof userRole !== "string" || typeof action !== "string") return false;\n` +
        `  const perms = ROLE_PERMISSIONS[userRole];\n` +
        `  const requestedTarget = typeof target === "string" ? target : undefined;\n` +
        `  return perms ? perms.some((permission) =>\n` +
        `    (permission.action === action || permission.action === "all") &&\n` +
        `    (permission.target === undefined || permission.target === requestedTarget)\n` +
        `  ) : false;\n` +
        `}`,
    );
  }
  if (/\bflag\(/.test(body)) {
    helpers.push(
      `// Feature toggle (configurable). Not an authorization gate.\nfunction flag(_name?: string): boolean { return true; }`,
    );
  }
  const serverImports = [
    ...(/= query\({/.test(body) || blocks.length === 0 ? ['query'] : []),
    ...(/= internalQuery\({/.test(body) ? ['internalQuery'] : []),
  ];
  const code =
    `${GENERATED_HEADER}\n// ${blocks.length} query function(s).\n\n` +
    `import { ${serverImports.join(', ')} } from "./_generated/server";\n` +
    `import { v } from "convex/values";\n` +
    (needsAuthCtx
      ? `import { getAuthContext } from ${JSON.stringify(options.authContextImport)};\n`
      : '') +
    (needsDecrypt ? `import { decrypt } from ${JSON.stringify(options.encryptionImport)};\n` : '') +
    `\n` +
    (needsDecrypt ? `${DECRYPT_HELPER}\n\n` : '') +
    (helpers.length ? `${helpers.join('\n\n')}\n\n` : '') +
    `${body}\n`;

  return { code, diagnostics };
}

