const ENCRYPT_HELPER = `async function __encryptDoc(ctx: any, entity: string, fields: readonly string[], doc: Record<string, any>): Promise<Record<string, any>> {
  const out = { ...doc };
  for (const property of fields) {
    const value = out[property];
    if (value == null) continue;
    const { ciphertext, keyId } = await encrypt(String(value), { ctx, entity, property });
    out[property] = JSON.stringify({ v: 1, kid: keyId, ct: ciphertext });
  }
  return out;
}`;

const DECRYPT_HELPER = `async function __decryptDoc(ctx: any, entity: string, fields: readonly string[], doc: any): Promise<any> {
  if (!doc) return doc;
  const out = { ...doc } as Record<string, any>;
  for (const property of fields) {
    const raw = out[property];
    if (typeof raw !== "string") continue;
    let envelope: any;
    try { envelope = JSON.parse(raw); } catch { continue; }
    const looksEncrypted = envelope && typeof envelope === "object" && "v" in envelope && "kid" in envelope && "ct" in envelope;
    if (!looksEncrypted) continue;
    if (envelope.v !== 1) throw new Error(\`Unsupported Manifest encryption envelope version: \${String(envelope.v)}\`);
    out[property] = await decrypt(envelope.ct, envelope.kid, { ctx, entity, property });
  }
  return out;
}`;

