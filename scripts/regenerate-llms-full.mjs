function collectFiles(dir, files = []) {
  const entries = readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    if (entry.isDirectory()) {
      collectFiles(join(dir, entry.name), files);
    } else if (entry.name.endsWith('.mdx')) {
      files.push(join(dir, entry.name));
    }
  }
  return files;
}

function getOrderKey(filePath) {
  const relativePath = filePath.replace(MINTLIFY_DIR + '/', '').replace(/\\/g, '/').replace('.mdx', '');
  const idx = ORDER.indexOf(relativePath);
  return idx === -1 ? 9999 : idx;
}

function formatUrl(relativePath) {
  // Convert Windows paths to web paths
  const cleanPath = relativePath.replace(/\\/g, '/').replace('.mdx', '');
  // Remove any leading "C:/projects/manifest/mintlify/" or similar
  const webPath = cleanPath.replace(/^.*[\\/]mintlify[\\/]/, '');
  return `${SOURCE_URL}/${webPath}`;
}

function extractTitle(content, filePath) {
  // Try to extract h1 title from content
  const h1Match = content.match(/^#\s+(.+)$/m);
  if (h1Match) return h1Match[1].trim();
  // Fallback to filename
  return filePath.replace(/\.mdx$/, '').replace(/.*[\\/]/, '').replace(/-/g, ' ');
}

async function main() {
  console.log('Regenerating llms-full.txt...');

  const files = collectFiles(MINTLIFY_DIR);
  files.sort((a, b) => getOrderKey(a) - getOrderKey(b));

  let output = `# Manifest — Full Documentation

> This file contains the complete text of all Manifest documentation pages.

> Source: ${SOURCE_URL}

> Package: @angriff36/manifest v${VERSION}

> Generated: ${new Date().toISOString().split('T')[0]}

<!-- NOTE: This file is automatically generated by scripts/regenerate-llms-full.mjs. Do not edit manually. -->


---
`;

  for (const file of files) {
    const relativePath = file.replace(MINTLIFY_DIR + '/', '').replace(/\\/g, '/').replace('.mdx', '');
    const content = readFileSync(file, 'utf-8');
    const title = extractTitle(content, relativePath);
    const url = formatUrl(relativePath);

    output += `\n## ${title}\n\n`;
    output += `URL: ${url}\n\n`;
    output += content;
    output += '\n\n---\n';
  }

  writeFileSync(OUTPUT_FILE, output, 'utf-8');

  const lines = output.split('\n').length;
  console.log(`✅ Generated ${OUTPUT_FILE} (${lines} lines)`);
  console.log(`   Version: ${VERSION}`);
  console.log(`   Files: ${files.length}`);
}

main().catch(console.error);
